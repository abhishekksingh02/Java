package com.basic;

public class IfElseLadderPractical {
	public static void main(String[] args) {
		int a=10;
		
		if(a==1) {
			System.out.println("True a==1");
		}else if (a==2) {
			System.out.println("True a==2");
		}else if (a==5) {
			System.out.println("True a==5");
		}else if (a==10) {
			System.out.println("True a==10");
		}else {
			System.out.println("True");
		}
	}
}
